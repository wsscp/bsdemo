package cc.oit.bsmes.common.constants;

public enum EquipStatus {
	
	/** for 设备、 */
	IN_PROGRESS("加工中"),
	IN_DEBUG("调试"), 
	/** 设备电源开启但没有运作 */
	IDLE("待机"),
	CLOSED("关机"), ERROR("故障"), IN_MAINTAIN("保养");

	private String status;

	private EquipStatus(String status) {
		this.status = status;
	}

	@Override
	public String toString() {
		return status;
	}

}
