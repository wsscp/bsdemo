package cc.oit.bsmes.common.constants;

/**
 * 
 * 库存明细状态，先分为可用和冻结2种状态
 * @author JinHanyun
 * @date 2014-1-7 上午10:46:42
 * @since
 * @version
 */
public enum InventoryDetailStatus {
	AVAILABLE("可用"),FREEZE("冻结");

	private String status;

	private InventoryDetailStatus(String status) {
		this.status = status;
	}

	@Override
	public String toString() {
		return status;
	}
}
