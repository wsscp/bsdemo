package cc.oit.bsmes.common.constants;

public enum WorkOrderOperateType {

	INSERT("新增生产单"), UPDATE("修改生产单"), DELETE("删除生产单"), ACCEPT("接受生产单"), FINISHED("完成生产单"),SWITCH("切换生产单");

	private String type;

	private WorkOrderOperateType(String type) {
		this.type = type;
	}

	@Override
	public String toString() {
		return type;
	}
	
}
