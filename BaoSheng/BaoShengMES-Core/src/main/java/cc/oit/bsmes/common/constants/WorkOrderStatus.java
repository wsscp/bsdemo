package cc.oit.bsmes.common.constants;

public enum WorkOrderStatus {
	
	TO_AUDIT("待下发"), TO_DO("已下发"), IN_PROGRESS("生产中"), CANCELED("已取消"), FINISHED("已完成"), PAUSE("已暂停");

	private String status;

	private WorkOrderStatus(String status) {
		this.status = status;
	}

	@Override
	public String toString() {
		return status;
	}
	
} 