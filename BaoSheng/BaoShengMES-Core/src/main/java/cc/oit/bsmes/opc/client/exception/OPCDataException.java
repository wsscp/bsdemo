package cc.oit.bsmes.opc.client.exception;

import cc.oit.bsmes.common.exception.MESException;

/**
 * 
 * <p style="display:none">表示数据提交失败。</p>
 * @author zhangdongping
 * @date 2014年3月20日 上午11:50:00
 * @since
 * @version
 */
public class OPCDataException extends MESException {

	private static final long serialVersionUID = 1669843560642800254L;

	public OPCDataException() {
		super("opc.OPCDataException");
	}

	public OPCDataException(String messageKey, Throwable cause) {
		super(messageKey, cause);
	}

	public OPCDataException(String messageKey) {
		super(messageKey);
	}

	public OPCDataException(Throwable cause) {
		super("opc.OPCDataException", cause);
	}

}
